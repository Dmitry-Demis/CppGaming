// Неинициализированные переменные и undefined behavior
// C++ НЕ инициализирует локальные переменные автоматически (в отличие от Java или C#).
// Использование неинициализированной переменной ведёт к неопределённому поведению (UB).

#include <iostream>

int main()
{// ПЛОХО: переменная x содержит случайный «мусор» из памяти.
	// Её использование — это undefined behavior (UB).
	// int x;
	// std::cout << x;  // UB! Результат непредсказуем: от случайного числа до падения программы.

	// ХОРОШО: всегда инициализируйте переменные при объявлении.
	int x{0};
	std::cout << "x = " << x << '\n';

	// Undefined behavior — это не просто «неправильный результат».
	// Компилятор вправе делать что угодно: программа может упасть, выдать неверные данные
	// или даже работать «правильно» (что хуже всего, так как скрывает ошибку до релиза).

	// Пример безопасной инициализации (value-initialization через пустые скобки):
	int a{};      // 0
	double b{};   // 0.0
	bool c{};     // false
	char d{};     // '\0' (нуль-символ)

	std::cout << "a=" << a << " b=" << b << " c=" << c << '\n';

	return 0;}

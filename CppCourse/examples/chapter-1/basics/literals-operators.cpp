// Литералы и операторы
// Литерал — фиксированное значение, записанное непосредственно в коде.
// Оператор — символ или конструкция, выполняющая операцию над операндами.

#include <iostream>

int main()
{// Литералы различных типов
	int    a{42};         // Целочисленный литерал
	double b{3.14};       // Литерал с плавающей точкой
	char   c{'A'};        // Символьный литерал
	bool   d{true};       // Булев литерал

	// Арифметические операторы
	// Результат вычисления выражения присваивается переменной
	int sum{a + 8};       // Сложение
	int diff{a - 10};     // Вычитание
	int prod{a * 2};      // Умножение
	int quot{a / 5};      // Деление (целочисленное, дробная часть отбрасывается)
	int rem{a % 5};       // Остаток от деления (только для целых чисел)

	std::cout << "a = " << a << '\n';
	std::cout << "a + 8 = " << sum << '\n';
	std::cout << "a - 10 = " << diff << '\n';
	std::cout << "a * 2 = " << prod << '\n';
	std::cout << "a / 5 = " << quot << '\n';
	std::cout << "a % 5 = " << rem << '\n';

	// Оператор вывода << тоже является оператором (побитовый сдвиг влево, перегруженный для потоков)
	std::cout << "char: " << c << ", bool: " << d << '\n';

	return 0;}

// Ключевые слова и именование идентификаторов
// Ключевые слова — зарезервированные слова языка C++, их нельзя использовать как имена.
// Идентификаторы — имена переменных, функций, типов и других сущностей.

#include <iostream>

int main()
{// Правильные имена идентификаторов
	// Рекомендуется использовать понятные имена на английском языке.
	int age{25};
	
	// camelCase: первое слово с маленькой буквы, последующие с большой
	double accountBalance{1500.50};
	
	// snake_case: слова разделяются нижним подчёркиванием
	int player_score{0};

	// Имена в C++ чувствительны к регистру (case-sensitive).
	// value и Value — это две совершенно разные переменные.
	int value{10};
	int Value{20};

	std::cout << "age = " << age << '\n';
	std::cout << "accountBalance = " << accountBalance << '\n';
	std::cout << "value = " << value << ", Value = " << Value << '\n';

	// Примеры ошибочных имен, которые не скомпилируются:
	// int 1stPlayer{0};      // Ошибка: имя не может начинаться с цифры.
	// int my-var{0};         // Ошибка: дефис (-) не допускается, это оператор вычитания.
	// int int{0};            // Ошибка: int — ключевое слово языка, его нельзя переопределять.

	return 0;}

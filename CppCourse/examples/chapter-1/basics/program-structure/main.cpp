// Структура программы
// Каждая программа на C++ должна иметь функцию main() — точку входа.
// Инструкции (statements) выполняются последовательно сверху вниз.

#include <iostream>  // подключаем библиотеку ввода/вывода

int main()           // точка входа программы
{
	// Это инструкция — выводит текст на экран
	std::cout << "Hello, World!\n";

	// Ещё одна инструкция
	std::cout << "Программа выполняется последовательно.\n";

	return 0;  // сообщаем ОС, что программа завершилась успешно}

}
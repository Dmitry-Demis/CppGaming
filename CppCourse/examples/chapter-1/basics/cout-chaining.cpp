// Цепочка вывода через <<
#include <iostream>

int main()
{
    // Несколько значений в одном выражении
    std::cout << "Hello" << " " << "world!" << "\n";

    int x{ 5 };
    std::cout << "x is equal to: " << x << "\n";

    // \n в разных формах — все эквивалентны:
    std::cout << "Строка 1\n";          // встроен в строку (обычно так)
    std::cout << "Строка 2" << "\n";    // отдельная строка (тоже ок)
    std::cout << "Строка 3" << '\n';    // символьный литерал (предпочтительно вне строк)

    return 0;
}

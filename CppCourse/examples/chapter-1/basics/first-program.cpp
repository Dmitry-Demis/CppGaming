// Первая программа: от задачи к коду
// Пример: программа запрашивает два числа и выводит их сумму.
// Это демонстрирует полный цикл разработки небольшой программы.

#include <iostream>

int main()
{// Объявляем переменные для хранения данных с нулевой инициализацией
	int a{ 0 };
	int b{ 0 };

	// Запрашиваем ввод первого числа
	std::cout << "Введите первое число: ";
	std::cin >> a;

	// Запрашиваем ввод второго числа
	std::cout << "Введите второе число: ";
	std::cin >> b;

	// Вычисляем сумму: выражение a + b возвращает значение, которое присваивается переменной sum
	int sum{ a + b };

	// Выводим результат в формате: число + число = сумма
	std::cout << a << " + " << b << " = " << sum << '\n';

	return 0;
}

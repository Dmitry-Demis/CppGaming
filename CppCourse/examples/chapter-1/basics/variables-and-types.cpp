// Переменные и типы данных
// Переменная — именованная область памяти для хранения значения.
// Каждая переменная имеет тип, определяющий, что именно в ней хранится и какие операции допустимы.

#include <iostream>

int main()
{
	// Объявление переменных без инициализации (содержат мусор, пока не присвоим значение)
	int age{};          // Целое число
	double price{};     // Вещественное число
	char letter{};      // Один символ
	bool isReady{};     // Логическое значение (true/false)

	// Рекомендуется объявлять переменные по одной для лучшей читаемости.
	// Избегайте стиля: int x, y, z; — это усложняет поддержку кода.
	int x{};
	int y{};
	int z{};

	// Объявление с инициализацией (предпочтительный способ)
	int count{ 10 };
	std::cout << "count = " << count << '\n';

	// Тип определяет размер в памяти (в байтах) и набор допустимых операций.
	// Оператор sizeof возвращает размер типа в байтах во время компиляции.
	std::cout << "Размер int: " << sizeof(int) << " байт\n";
	std::cout << "Размер double: " << sizeof(double) << " байт\n";

	return 0;
}

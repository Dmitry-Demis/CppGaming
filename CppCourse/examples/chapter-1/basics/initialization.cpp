// Инициализация и присваивание
// В C++ существуют различные способы создания переменных с начальным значением.

#include <iostream>

int main()
{
	// Copy-initialization (копирующая) — стиль языка C.
	// Допускает неявные сужающие преобразования, что может быть опасно.
	int a = 5;

	// Direct-initialization (прямая) — вызов конструктора со скобками.
	int b(10);

	// List-initialization (списочная) — современный стандарт C++11 и новее.
	// Предпочтительный способ: запрещает сужающие преобразования, повышая безопасность.
	int c{ 15 };
	
	// Value-initialization: если список пуст, переменная обнуляется.
	int d{};    // d == 0

	// Вывод значений с выравниванием для наглядности
	std::cout << "a = " << a << '\n'
	          << "b = " << b << '\n'
	          << "c = " << c << '\n'
	          << "d = " << d << '\n';

	// Присваивание — это изменение значения уже существующей переменной.
	// Оператор '=' вычисляет выражение справа и записывает результат в память слева.
	int x{ 1 };
	x = 42;     // Теперь x хранит 42
	std::cout << "x = " << x << '\n';

	// List-initialization защищает от случайной потери данных при сужении типов.
	// Раскомментированная строка ниже вызовет ошибку компиляции:
	// int e{ 3.14 };  // Ошибка: сужение double до int недопустимо в фигурных скобках.
	
	// Корректное объявление вещественного числа:
	double pi{ 3.14 };
	std::cout << "pi = " << pi << '\n';

	return 0;
}
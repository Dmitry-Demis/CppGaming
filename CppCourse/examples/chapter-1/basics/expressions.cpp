// Выражения (expressions)
// Выражение — любая комбинация литералов, переменных и операторов,
// которая вычисляется в единственное значение определённого типа.

#include <iostream>

int main()
{
	// Предпочитаем brace-initialization (без лишних пробелов внутри скобок)
	int x{ 5 };
	int y{ 3 };

	// x + y, x * 2 + y и т.д. — это выражения. Они вычисляются в значение.
	// Когда мы записываем это значение в переменную через '=' или '{...}',
	// вся строка становится инструкцией объявления с инициализацией.
	int a{ x + y };              // Выражение: x + y (результат 8)
	int b{ x * 2 + y };          // Выражение: x * 2 + y (результат 13)
	int c{ (x + y) * 2 };        // Выражение: (x + y) * 2 (результат 16)

	std::cout << "x + y = " << a << '\n';
	std::cout << "x * 2 + y = " << b << '\n';
	std::cout << "(x + y) * 2 = " << c << '\n';

	// Выражение можно встраивать напрямую — вычисление произойдёт «на лету»
	// Здесь выражение x * x + y * y не присваивается переменной, а сразу передаётся в поток.
	std::cout << "Прямо в cout: " << x * x + y * y << '\n';

	// Тип выражения определяется правилами C++ на основе типов операндов,
	// а не типом переменной, куда (если вообще куда) записывается результат.

	// Пример 1: Один операнд double → всё выражение вычисляется как double
	double result{ 7.0 / 2 };    // 3.5

	// Пример 2: Оба операнда int → целочисленное деление (дробная часть отбрасывается)
	// Даже если мы запишем результат в double, выражение 7 / 2 всё равно даст 3.
	// Это частая ошибка: потеря точности происходит до момента присваивания.
	double lost_precision{ 7 / 2 }; // 3.0 (неверно ожидалось 3.5)
	int    trunc{ 7 / 2 };          // 3

	std::cout << "7.0 / 2 = " << result << " (корректно)\n";
	std::cout << "7 / 2 (запись в double) = " << lost_precision << " (потеря точности!)\n";
	std::cout << "7 / 2 (запись в int) = " << trunc << '\n';

	// 💡 Запомни: выражение ≠ инструкция.
	// Выражение вычисляется в значение (например, 5, x + y, func()).
	// Инструкция — это законченное действие программы (часто: выражение + ';').
	// Понимание этой разницы поможет избежать ошибок с побочными эффектами и порядком вычислений.

	return 0;
}

#include <iostream>
#include <cstdlib>

// Функция очистки, зарегистрированная через std::atexit
void cleanup()
{std::cout << "Очистка ресурсов перед завершением\n";}

void anotherCleanup()
{std::cout << "Ещё одна функция очистки\n";}

int main()
{// std::atexit регистрирует функцию, вызываемую при нормальном завершении
    // Функции вызываются в порядке, обратном регистрации (LIFO)
    std::atexit(cleanup);
    std::atexit(anotherCleanup);

    std::cout << "Программа работает...\n";

    // std::exit завершает программу нормально:
    // - вызывает зарегистрированные atexit-функции
    // - сбрасывает буферы потоков
    // - НЕ уничтожает локальные переменные (деструкторы не вызываются)
    std::exit(0);

    // Эта строка никогда не выполнится
    std::cout << "Недостижимый код\n";}

// Вывод программы:
// Программа работает...
// Ещё одна функция очистки   <- зарегистрирована второй, вызывается первой
// Очистка ресурсов перед завершением


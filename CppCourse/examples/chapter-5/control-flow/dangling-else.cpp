#include <iostream>

int main()
{std::cout << "Введите число: ";
    int x{};
    std::cin >> x;

    // ПРОБЛЕМА: к какому if привязан else?
    // Ответ: к ближайшему незакрытому — то есть к внутреннему if (x <= 20).
    // При x = 21 выведет "21 — отрицательное", что неверно!
    if (x >= 0)
        if (x <= 20)
            std::cout << x << " от 0 до 20\n";
        else                                      // привязан к (x <= 20), не к (x >= 0)!
            std::cout << x << " — отрицательное\n"; // BUG

    std::cout << "--- исправленная версия ---\n";

    // РЕШЕНИЕ: явные блоки устраняют неоднозначность
    if (x >= 0)
    {if (x <= 20)
            std::cout << x << " от 0 до 20\n";
        else
            std::cout << x << " больше 20\n";}
    else
    {std::cout << x << " — отрицательное\n";}

    return 0;}


#include <iostream>

// Ветвление: путь выполнения зависит от данных времени выполнения.
// Одна и та же программа может пойти по разным путям.

int main()
{std::cout << "Введите число: ";
    int x{};
    std::cin >> x;

    // Условный оператор — первая категория управления потоком
    if (x > 0)
        std::cout << x << " — положительное\n";
    else if (x < 0)
        std::cout << x << " — отрицательное\n";
    else
        std::cout << "Ноль\n";

    // Цикл — повторяет блок, пока условие истинно
    int count{x};
    while (count > 0)
    {std::cout << count << ' ';
        --count;}
    if (x > 0) std::cout << '\n';

    return 0;}


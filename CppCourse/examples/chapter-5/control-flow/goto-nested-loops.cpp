#include <iostream>

int main()
{// Вариант 1: goto для выхода из вложенных циклов
    // Единственный случай, где goto может быть оправдан
    for (int i {0}; i < 3; ++i)
    {for (int j {0}; j < 3; ++j)
        {if (i == 1 && j == 1)
                goto done; // выход из обоих циклов сразу
            std::cout << i << "," << j << "\n";}}
done:
    std::cout << "Выход из вложенных циклов\n";

    // Вариант 2: альтернатива через флаг (без goto)
    bool found {false};
    for (int i {0}; i < 3 && !found; ++i)
    {for (int j {0}; j < 3 && !found; ++j)
        {if (i == 1 && j == 1)
                found = true;
            else
                std::cout << i << "," << j << " (флаг)\n";}}
    std::cout << "Выход через флаг\n";

    // Вариант 3: альтернатива через вынос в функцию (предпочтительно)
    // Функция возвращает управление при первом совпадении

    return 0;}

